    private void buscar() {
        try {
            String metodo = cbxMetodo.getSelectedItem().toString();
            String clave = txtBusqueda.getText().toString();
            String atributo = cbxAtributos.getSelectedItem().toString();
            switch (metodo) {
                case "Binaria":
                    modelo.setLista(ed.buscarArtistasBinario(ed.listar(), clave, atributo));
                    break;
                case "Lineal Binaria":
                    modelo.setLista(ed.buscarArtistaLinealBinaria(ed.listar(), clave, atributo));
                    break;
            }
            tblartistas.setModel(modelo);
            tblartistas.updateUI();
            System.out.println("busqueda finalizada");
        } catch (Exception ex) {
            Logger.getLogger(FrmArtista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

